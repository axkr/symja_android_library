package de.tilman_neumann.util;

import java.io.Serializable;

/**
 * A simple utility class combining two values of arbitrary types to one object.
 * @author Tilman Neumann
 * 
 * @param <U> type of first value
 * @param <V> type of second value
 */
public class Pair<U,V> implements Serializable {

	private static final long serialVersionUID = 6122478067737520852L;
	
	private U first;
	private V second;
	
	public Pair(U u, V v) {
		first = u;
		second = v;
	}
	
	public U getFirst() {
		return first;
	}
	
	public V getSecond() {
		return second;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((first == null) ? 0 : first.hashCode());
		result = prime * result + ((second == null) ? 0 : second.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Pair other = (Pair) obj;
		if (first == null) {
			if (other.first != null)
				return false;
		} else if (!first.equals(other.first))
			return false;
		if (second == null) {
			if (other.second != null)
				return false;
		} else if (!second.equals(other.second))
			return false;
		return true;
	}
}
