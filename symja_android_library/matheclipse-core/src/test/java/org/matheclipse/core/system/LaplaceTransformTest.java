  @Test
  public void testInverseLaplaceTransform() {
    // 1. The untouched base fraction
    check("InverseLaplaceTransform(1/(E^(Pi*s)+E^(Pi*s)*s^2), s, t)", //
        "-HeavisideTheta(-Pi+t)*Sin(t)");

    // 2. The C(2) term that failed to cancel.
    // It SHOULD cancel E^(Pi*s) automatically and return C(2)*Sin(t).
    check("InverseLaplaceTransform((E^(Pi*s)*C(2))/(E^(Pi*s)+E^(Pi*s)*s^2), s, t)", //
        "C(2)*Sin(t)");

    // 3. The C(1) term that failed to cancel.
    // It SHOULD cancel E^(Pi*s) automatically and return C(1)*Cos(t).
    check("InverseLaplaceTransform((E^(Pi*s)*s*C(1))/(E^(Pi*s)+E^(Pi*s)*s^2), s, t)", //
        "C(1)*Cos(t)");

    // 4. The full combined raw fraction generated by the Solve engine.
    // Proves the algebraic preparation pipeline can split, cancel, and transform it flawlessly.
    check(
        "InverseLaplaceTransform((1+E^(Pi*s)*s*C(1)+E^(Pi*s)*C(2))/(E^(Pi*s)+E^(Pi*s)*s^2), s, t)", //
        "C(1)*Cos(t)+C(2)*Sin(t)-HeavisideTheta(-Pi+t)*Sin(t)");

    // Tests that complex numerators are distributed alongside factored denominators
    // L^-1{ (1 + s) / (E^(Pi*s) + E^(Pi*s)*s^2) }
    check("InverseLaplaceTransform((1+s)/(E^(Pi*s)+E^(Pi*s)*s^2), s, t)", //
        "HeavisideTheta(-Pi+t)*(-Cos(t)-Sin(t))");

    check("InverseLaplaceTransform(1/(s^3+2*s^2+s),s,t)", //
        "1-1/E^t-t/E^t");
    check("InverseLaplaceTransform(f(x)*s,s,t)", //
        "f(x)*DiracDelta'(t)");
    check("InverseLaplaceTransform(f(x),s,t)", //
        "DiracDelta(t)*f(x)");
    check("InverseLaplaceTransform(1/s,s,t)", //
        "1");
    check("InverseLaplaceTransform(1/s^5,s,t)", //
        "t^4/24");
    check("InverseLaplaceTransform(1/(s^2 +a^2),s,t)", //
        "Sin(a*t)/a");
    check("InverseLaplaceTransform(s/(s^2 +a^2),s,t)", //
        "Cos(a*t)");
    check("InverseLaplaceTransform(1/(1+s),s,t)", //
        "E^(-t)");
    check("InverseLaplaceTransform(1/(s^2-4),s,t)", //
        "-1/(4*E^(2*t))+E^(2*t)/4");
    // test partial fraction decomposition:
    check("InverseLaplaceTransform(Together(3/(s-1)+(2*s)/(s^2+4)),s,t)", //
        "3*E^t+2*Cos(2*t)");
    check("InverseLaplaceTransform(3/(s-1)+(2*s)/(s^2+4),s,t)", //
        "3*E^t+2*Cos(2*t)");
  }


